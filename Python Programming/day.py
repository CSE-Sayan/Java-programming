day=int(input("Enter a number from 1 to 7: "))
if(day==1):
    print(day, "is Sunday")
if(day==2):
    print(day, "is Monday")
if(day==3):
    print(day, "is Tuesday")
if(day==4):
    print(day, "is Wednesday")
if(day==5):
    print(day, "is Thursday")
if(day==6):
    print(day, "is Friday")
if(day==7):
    print(day, "is Saturday")
else:
    print("Wrong input! Please enter a number from 1 to 7.")